// Very simple, given an integer or a floating-point number, find its opposite.


// Parameters
// the function is being passed a positive or negative number
// returns 
// returns the opposite of the number passed as an argument

// examples:
// 1: -1
// 14: -14
// -34: 34

// pseudocode

// if the number is less than 0 return the number * -1 to make it positive 
// else make the number negative by multiplying times -1


const opposite = number => number < 0 ? number * -1 : number * -1
